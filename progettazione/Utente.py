# Specifica della classe Utente
# [V.Utente.bidDopoRegistrazione] Un utente non può avere bid prima di essere registrato.
# Per ogni u:Utente sia b:Bid tale che (b,u): bid_ut deve essere u.registrazione < b.istante.
# [V.Utente.pubblicazioneDopoRegistrazione] Un utente non può pubblicare post prima di essere registrato.